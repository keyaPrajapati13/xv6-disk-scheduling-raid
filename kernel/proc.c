struct proc proc[NPROC];
int getnumchild(struct proc *p)
{
  struct proc *pp;
  int count = 0;
  for (pp = proc; pp < &proc[NPROC]; pp++)
  {
    if (pp->parent == p && pp->state != ZOMBIE && pp->state != UNUSED)
    {
      count++;
    }
  }
  return count;
}
